let alunos = ['Gabriel', 'Lobato', 'Eu', 'Pedro']
console.log(alunos);
//Remove o último elemento do  nosso vetor
console.log('== POP ==');
alunos.pop()
console.log(alunos);

//Remove o primeiro elemneto do nosso vetor
console.log('== SHIFT ==');
alunos.shift()
console.log(alunos);

//Adicionar algo no final do nosso vetor
console.log('== PUSH ==');
alunos.push('Yago')
console.log(alunos);

//Adicionar algo no inicío do nosoo vetor
console.log("== unshift==");
alunos.unshift("Luiz")
console.log(alunos);